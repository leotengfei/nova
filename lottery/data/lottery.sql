/*资格列表*/
CREATE TABLE lotterylist(
    lid INT PRIMARY KEY AUTO_INCREMENT,
    stel VARCHAR(32) NOT NULL DEFAULT '',
    sname VARCHAR(10) NOT NULL DEFAULT '',
    count INT NOT NULL DEFAULT 0
);
