SET NAMES UTF8;
CREATE TABLE lvote_msg(
	vid INT PRIMARY KEY AUTO_INCREMENT, -- 作品id
	vname VARCHAR(50) NOT NULL DEFAULT '', -- 学员姓名
	pic VARCHAR(100) NOT NULL DEFAULT '', -- 作品图片
	vnum INT NOT NULL DEFAULT 0 -- 票数
);
CREATE TABLE lvote_record(
	rid INT PRIMARY KEY AUTO_INCREMENT,
	ruid VARCHAR(100) NOT NULL DEFAULT '',
	rtime VARCHAR(50) NOT NULL DEFAULT ''
);
CREATE TABLE lbaoming(
	bid INT PRIMARY KEY AUTO_INCREMENT,
	bname VARCHAR(50) NOT NULL DEFAULT '', -- 报名姓名
	btel VARCHAR(50) NOT NULL DEFAULT '', -- 电话号码
    bclass VARCHAR(50) NOT NULL DEFAULT '' -- 年级
);
CREATE TABLE lzixun(
	zid INT PRIMARY KEY AUTO_INCREMENT,
	zname VARCHAR(50) NOT NULL DEFAULT '', -- 咨询电话
	ztel VARCHAR(50) NOT NULL DEFAULT '' -- 电话号码
);