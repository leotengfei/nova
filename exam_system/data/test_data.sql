SET NAMES UTF8;
CREATE TABLE test_user(
	uid INT PRIMARY KEY AUTO_INCREMENT,
	uname VARCHAR(50) NOT NULL DEFAULT '',
	utel VARCHAR(50) NOT NULL DEFAULT '',
	area VARCHAR(20) NOT NULL DEFAULT '',
	identify VARCHAR(20) NOT NULL DEFAULT '',
	school VARCHAR(100) NOT NULL DEFAULT '',
	subject VARCHAR(20) NOT NULL DEFAULT '',
	team_id VARCHAR(50) NOT NULL DEFAULT ''
);
CREATE TABLE test_score(
	sid INT PRIMARY KEY AUTO_INCREMENT,
	uid INT NOT NULL DEFAULT 0,
	score VARCHAR(20) NOT NULL DEFAULT '',
	test_date VARCHAR(50) NOT NULL DEFAULT ''
);
CREATE TABLE test_paper(
	paperID INT PRIMARY KEY AUTO_INCREMENT,
	course VARCHAR(50) NOT NULL DEFAULT '',
	type VARCHAR(20) NOT NULL DEFAULT '',
	title_num INT NOT NULL DEFAULT 0
);
CREATE TABLE test_answer(
	aid INT PRIMARY KEY AUTO_INCREMENT,
	paperID INT NOT NULL DEFAULT 0,
	title VARCHAR(1000) NOT NULL DEFAULT '',
	answerA VARCHAR(500) NOT NULL DEFAULT '',
	answerB VARCHAR(500) NOT NULL DEFAULT '',
	answerC VARCHAR(500) NOT NULL DEFAULT '',
	answerD VARCHAR(500) NOT NULL DEFAULT '',
	answer VARCHAR(50) NOT NULL DEFAULT ''
);